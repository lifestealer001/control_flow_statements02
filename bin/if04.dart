/*   
Create function called func
Return zero if the numbers are equal, return the larger one if they are not equal.
Args:
    a: First number.
    b: Second number.
Returns:
    int: return answer. 
*/
String func(int a,b){
    String answer = '';
    if(a == b){
        return answer = 'zero';
    }else{
        return answer = 'one';
    }
}
void main() {
    print (func(5,5));
}
